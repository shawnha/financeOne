def main():
    db_url = os.environ["DATABASE_URL"]
    conn = psycopg2.connect(db_url)
    try:
        cur = conn.cursor()
        cur.execute("SET search_path TO financeone, public")

        # 1. description 컬럼 추가 (이미 있으면 skip)
        cur.execute("""
            ALTER TABLE standard_accounts
            ADD COLUMN IF NOT EXISTS description TEXT
        """)
        print("✓ description 컬럼 확인 / 추가")

        # 2. 일괄 UPDATE
        updated = 0
        skipped = 0
        for code, desc in DESCRIPTIONS:
            cur.execute(
                "UPDATE standard_accounts SET description = %s WHERE code = %s RETURNING id",
                [desc, code],
            )
            if cur.fetchone():
                updated += 1
            else:
                print(f"  ⚠ code={code} 매칭 실패 (skip)")
                skipped += 1

        conn.commit()
        cur.close()

        print(f"\n✓ {updated} 계정 description 입력 완료 (skip {skipped})")

    except Exception as e:
        conn.rollback()
        print(f"✗ 실패: {e}")
        raise
    finally:
        conn.close()


if __name__ == "__main__":
    main()
