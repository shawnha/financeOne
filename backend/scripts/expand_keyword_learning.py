def main() -> None:
    db_url = os.environ["DATABASE_URL"]
    conn = psycopg2.connect(db_url)
    try:
        cur = conn.cursor()
        cur.execute("SET search_path TO financeone, public")

        # 스키마: keyword 자체가 UNIQUE — 동일 키워드가 두 계정에 매핑 불가.
        # 동일 키워드 충돌 시 skip (기존 매핑 보존). 동일 keyword + 동일 std_id 도 skip.

        # code → id 룩업
        cur.execute("SELECT code, id FROM standard_accounts")
        code_to_id = {r[0]: r[1] for r in cur.fetchall()}

        inserted = 0
        skipped_existing = 0
        skipped_no_code = 0
        seen_keywords = set()
        for kw, code, conf in DOMAIN_KEYWORDS:
            if kw in seen_keywords:
                # 같은 키워드가 스크립트 내에서 중복 (예: "주유" 2번) → 첫 매핑만 적용
                skipped_existing += 1
                continue
            seen_keywords.add(kw)

            std_id = code_to_id.get(code)
            if not std_id:
                skipped_no_code += 1
                print(f"  ! code={code} not found, skipping keyword={kw}")
                continue

            cur.execute(
                """
                INSERT INTO standard_account_keywords (keyword, standard_account_id, confidence)
                VALUES (%s, %s, %s)
                ON CONFLICT (keyword) DO NOTHING
                RETURNING id
                """,
                [kw, std_id, conf],
            )
            if cur.fetchone():
                inserted += 1
            else:
                skipped_existing += 1

        conn.commit()

        cur.execute("SELECT COUNT(*) FROM standard_account_keywords")
        total = cur.fetchone()[0]
        cur.close()

        print(f"\n신규 INSERT: {inserted}건")
        print(f"이미 존재: {skipped_existing}건")
        print(f"코드 없음: {skipped_no_code}건")
        print(f"전체 standard_account_keywords: {total}개")

    except Exception as e:
        conn.rollback()
        print(f"실패: {e}")
        raise
    finally:
        conn.close()


if __name__ == "__main__":
    main()
